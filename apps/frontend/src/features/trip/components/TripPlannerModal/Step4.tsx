export function Step4({ onNextStep }: Step4Props) {
  const [pickedThemes, setPickedThemes] = useState<string[]>([]);
  return (
    <div className="p-4">
      <div className="mt-5 flex flex-col items-center gap-4">
        <IonImg class="h-[60px] w-[60px]" src={"/images/fly.svg"} />
        <div className="text-xl font-light">Travel Theme</div>
        <div className="text-center  text-xs text-[#aaa]">
          You can select more than one
        </div>
      </div>

      <div className="mt-4 flex flex-col gap-4">
        <div className="grid grid-cols-2 gap-4">
          {travelThemes.map((theme, index) => (
            <div
              key={index}
              onClick={() => {
                if (pickedThemes.includes(theme.name)) {
                  setPickedThemes(
                    pickedThemes.filter((item) => item !== theme.name),
                  );
                } else {
                  setPickedThemes([...pickedThemes, theme.name]);
                }
              }}
              className={cn(
                "flex h-[80px] flex-col items-center justify-center gap-2 rounded-lg border border-current",
                pickedThemes.includes(theme.name)
                  ? "text-primary"
                  : "text-gray-300",
              )}
            >
              <div className="text-sm">{theme.name}</div>
              <div>{theme.icon}</div>
            </div>
          ))}
        </div>

        <div className="flex items-center justify-center gap-2">
          <IonButton className="w-full" onClick={() => onNextStep()}>
            Next
          </IonButton>
        </div>
      </div>
    </div>
  );
}
